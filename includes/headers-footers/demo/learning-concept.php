<p> The OU Careers Advisory Service provides professional careers and education advisers to support your course choice and career planning.   You can contact us for advice on planning, developing or changing your career, deciding what type of work would best suit you, completing application forms, CVs or covering letters, preparing for an interview, looking for employment, getting involved in voluntary work, considering further study or professional training, leaving your course or changing direction.</p>
