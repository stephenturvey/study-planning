<h3><a href="http://www.open.ac.uk/family/">Friends and family can help too</a></h3>
<p> Your partner, friend or relative has decided to study an Open University course and you may be wondering about the effects that will have on your lives. This site explains how things might change and how you can help your OU student to be successful. </p>
<h3><a href="http://www.open.ac.uk/skillsforstudy/english-for-learning.php">English as an additional language</a></h3>
<p> Are you thinking about starting an OU course but are worried that your English language and academic skills are not going to be good enough? English for learning provides support and guidance on improving your English skills and helps you discover how academic English is used in our courses. </p>
